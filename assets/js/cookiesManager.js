/* 
L’option samesite empêche le navigateur d’envoyer un cookie lors d’une requête cross-site.
Cette option offre une bonne protection contre les attaques de type XSRF.

L’option secure permet d’indiquer qu’un cookie doit être envoyé uniquement via HTTPS et ne
 pas l’être via HTTP. Cette option est très utile si un cookie possède des données sensibles qui ne doivent pas être envoyées sans encryptage. 
*/
function createCookie(name, value, days) {
    let expires = "";
    if (days) {
        let date = new Date();
        date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
        expires = "; expires=" + date.toUTCString();
    }
    document.cookie = name + "=" + value + expires + "; path=/" + "; SameSite=Strict; secure";
    console.log(document.cookie)

}

function readCookie(name) {
    let nameEQ = name + "=";
    let ca = document.cookie.split(';');
    for (let i = 0; i < ca.length; i++) {
        let c = ca[i];
        while (c.charAt(0) == ' ') {
            c = c.substring(1, c.length);
        }
        if (c.indexOf(nameEQ) == 0) {
            return c.substring(nameEQ.length, c.length);
        }
    }
    return false;
}

function eraseCookie(name) {
    createCookie(name, "", -1);
}

// Mise à jour du tableau (CookiesList) au chargement de la page en fonction du choix de l'utilisateur 
const checkedArrayCookies = (fileCookieContent) => {
    // Parcours le tableau liste des cookies
    CookiesList.forEach((categoryItem) => {
        // Recherche les cookies autorisés par l'utilisateur
        categoryItem.scripts.forEach(scriptItem => {
            if( fileCookieContent.find(fileCookieItem => fileCookieItem === scriptItem.id)) {
                // Cookie autorisé par l'utilisateur
                scriptItem.checked = true;
                // Injecte les scripts dans la balise head de la page
                injectScriptsHead(scriptItem);
            } else {
                // Cookie rejeté par l'utilisateur
                scriptItem.checked = false;
            }
        })
    });
}

// Injection des scripts dans l'en tete de la page
const addCookiesUrl = (url) => {
    var scriptCookiesUrl = document.createElement("script"); // create a script DOM node
    scriptCookiesUrl.src = url; //set its src to the provided URL
    document.head.appendChild(scriptCookiesUrl);
}

const addCookiesParameters = (parameters) => {
    var scriptCookiesParameters = document.createElement("script"); // create a script DOM node
    scriptCookiesParameters.textContent = parameters; //set its src to the provided URL
    document.head.appendChild(scriptCookiesParameters);
}

// Injecte les scripts dans la balise head de la page
const injectScriptsHead = (script) => {
    //  injecte les scripts dans head
    if (script.script_src) {
        addCookiesUrl(script.script_src);
    }
    //  injecte la config dans le script
    if (script.script_config) {
        addCookiesParameters(script.script_config);
    }
}

